public class Node {
    char data;
    Node left;
    Node right;
    public char getData() {
        return data;
    }
    public void setData(char data) {
        this.data = data;
    }
    public Node getLeft() {
        return left;
    }

    public void setLeft(Node left) {
        this.left = left;
    }

    public Node getRight() {
        return right;
    }

    public void setRight(Node right) {
        this.right = right;
    }
}
